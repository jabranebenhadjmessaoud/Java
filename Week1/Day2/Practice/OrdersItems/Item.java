
public class Item {

    private String name ;
    private Double price ;
    public Item (){} ;
    public String getName(){
        return this.name;
     }
    public Double getPrice(){
        return this.price;
     }
    public void setName(String name) {
        this.name = name;
    }
    public void setPrice(Double price) {
        this.price = price;
    
    }

}
